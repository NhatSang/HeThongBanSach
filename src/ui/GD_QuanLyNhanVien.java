    private void loadnv() {
        while(tablenv.getRowCount()!=0) {
			modelnv.removeRow(0);
		}
    	NhanVien_DAO nv = new NhanVien_DAO();
    	String gt;
    	ArrayList<NhanVien> dsnv = nv.getAllNV();
    	for (NhanVien nhanvien : dsnv) {
    		if(nhanvien.isGioiTinh()== false) {
    			gt = "Nữ";
    		}else
    			gt = "Nam";
			Object row[] = {++stt, nhanvien.getMaNV(), nhanvien.getHoTen(), nhanvien.getNgaySinh(), nhanvien.getCccd(),nhanvien.getDiaChi(),nhanvien.getSdt(), gt,null,nhanvien.getChucVu().getTenCV()};
			modelnv.addRow(row);
		}
    }
    public void loadTbltoForm(int row) {
        System.out.println(row);
		txtma.setText(tablenv.getValueAt(row, 1).toString());
		txthoten.setText(tablenv.getValueAt(row, 2).toString());
		java.util.Date ngaySinh = null;
		try {
			ngaySinh = new SimpleDateFormat("yyyy-MM-dd").parse(tablenv.getValueAt(row, 3).toString());
		} catch (Exception e) {
			// TODO: handle exception
		}
		jdcngaysinh.setDate(ngaySinh);
		txtcccd.setText(tablenv.getValueAt(row, 4).toString());
		txtdiachi.setText(tablenv.getValueAt(row, 5).toString());
		txtsdt.setText(tablenv.getValueAt(row, 6).toString());
		if (tablenv.getValueAt(row, 7).toString().equals("Nam"))
			radnam.setSelected(true);
		else
			radnu.setSelected(true);
		txttaikhoan.setText(tablenv.getValueAt(row, 8).toString());
		cbbchucvu.setSelectedItem(tablenv.getValueAt(row, 9));
		setEditableForm(true);
	}
