    public boolean checkThongTin(){
        String ten = txthoten.getText();
        String dc = txtdiachi.getText();
        String sdt = txtsdt.getText();
        String today = Date.valueOf(LocalDate.now()).toString();        
        if(ten.equals("")){
                txtthongbao.setText("Nhập họ tên khách hàng");
                return false;
            }
        if(jdcngaysinh.getDate()==null){
        	txtthongbao.setText("Nhập ngày sinh khách hàng");
            return false;
            }
        if(dc.equals("")){
        	txtthongbao.setText("Nhập địa chỉ khách hàng");
                return false;
            }
        if(!sdt.matches("[0-9]{10}")){
        	txtthongbao.setText("SĐT phải đúng định dạng 0xxxxxxxxx");
                return false;
            }
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String ns = dateFormat.format(jdcngaysinh.getDate());
        int a = ns.compareTo(today);
                System.out.println(a);
        if(a > 0){
        	txtthongbao.setText("Ngày sinh phải bé hơn ngày hiện tại");
                        return false;
                    }
        return true;
    }
